/**
 * Run benchmarks and generate PERFORMANCE.md report.
 * Usage: pnpm bench:report
 */

import { resolve } from 'path'
import { readFileSync, writeFileSync } from 'fs'
import { execSync } from 'child_process'
import { platform, arch, cpus, totalmem } from 'os'

interface BenchResult {
  name: string
  hz: number
  mean: number
  min: number
  max: number
  p75: number
  p99: number
  rme: number
  samples: number
}

function parseBenchOutput(output: string): Map<string, BenchResult[]> {
  const groups = new Map<string, BenchResult[]>()
  let currentGroup = ''
  const seen = new Set<string>()

  for (const line of output.split('\n')) {
    // Match group headers like "✓ benchmarks/measure.bench.ts > Cold Start"
    const groupMatch = line.match(/[>✓]\s+benchmarks\/\S+\s+>\s+(.+?)(?:\s+\d+ms)?$/)
    if (groupMatch) {
      currentGroup = groupMatch[1]!.trim()
      if (!groups.has(currentGroup)) groups.set(currentGroup, [])
      continue
    }

    // Match benchmark result lines
    const resultMatch = line.match(/^\s+·\s+(.+?)\s{2,}([\d,.]+)\s+([\d.]+)\s+([\d.]+)\s+([\d.]+)\s+([\d.]+)\s+([\d.]+)/)
    if (resultMatch && currentGroup) {
      const name = resultMatch[1]!.trim()
      const dedupeKey = `${currentGroup}::${name}`
      if (seen.has(dedupeKey)) continue
      seen.add(dedupeKey)

      const results = groups.get(currentGroup)!
      const hz = parseFloat(resultMatch[2]!.replace(/,/g, ''))
      const mean = parseFloat(resultMatch[5]!)
      results.push({
        name,
        hz,
        mean,
        min: parseFloat(resultMatch[3]!),
        max: parseFloat(resultMatch[4]!),
        p75: parseFloat(resultMatch[6]!),
        p99: parseFloat(resultMatch[7]!),
        rme: 0,
        samples: 0,
      })
    }
  }

  return groups
}

function formatMs(ms: number): string {
  if (ms < 0.01) return `${(ms * 1000).toFixed(1)}μs`
  if (ms < 1) return `${ms.toFixed(2)}ms`
  if (ms < 1000) return `${ms.toFixed(1)}ms`
  return `${(ms / 1000).toFixed(2)}s`
}

function formatOps(hz: number): string {
  if (hz >= 1_000_000) return `${(hz / 1_000_000).toFixed(1)}M ops/s`
  if (hz >= 1_000) return `${(hz / 1_000).toFixed(1)}K ops/s`
  return `${hz.toFixed(1)} ops/s`
}

function generateReport(groups: Map<string, BenchResult[]>): string {
  const cpu = cpus()[0]
  const nodeVersion = process.version
  const date = new Date().toISOString().split('T')[0]

  let md = `# Textric Performance Report

> Auto-generated by \`pnpm bench:report\` — do not edit manually.
>
> **Environment**: ${platform()} ${arch()}, ${cpu?.model ?? 'unknown'}, Node.js ${nodeVersion}
> **Date**: ${date}

`

  // Cold Start
  const coldStart = groups.get('Cold Start')
  if (coldStart) {
    md += `## Cold Start\n\n`
    md += `| Operation | Mean | Min |\n|-----------|------|-----|\n`
    for (const r of coldStart) {
      md += `| ${r.name} | ${formatMs(r.mean)} | ${formatMs(r.min)} |\n`
    }
    md += '\n'
  }

  // Single-line
  const singleLine = groups.get('Single-line Measurement')
  if (singleLine) {
    md += `## Single-line Measurement (font cached)\n\n`
    md += `| Text | Mean | Throughput |\n|------|------|------------|\n`
    for (const r of singleLine) {
      md += `| ${r.name} | ${formatMs(r.mean)} | ${formatOps(r.hz)} |\n`
    }
    md += '\n'
  }

  // Multi-line
  const multiLine = groups.get('Multi-line Wrapping (maxWidth: 300px)')
  if (multiLine) {
    md += `## Multi-line Wrapping (maxWidth: 300px)\n\n`
    md += `| Text | Mean | Throughput |\n|------|------|------------|\n`
    for (const r of multiLine) {
      md += `| ${r.name} | ${formatMs(r.mean)} | ${formatOps(r.hz)} |\n`
    }
    md += '\n'
  }

  // Rich Text
  const richText = groups.get('Rich Text (5 spans, ~120 chars)')
  if (richText) {
    md += `## Rich Text (5 spans, ~120 chars)\n\n`
    md += `| Mode | Mean | Throughput |\n|------|------|------------|\n`
    for (const r of richText) {
      md += `| ${r.name} | ${formatMs(r.mean)} | ${formatOps(r.hz)} |\n`
    }
    md += '\n'
  }

  // Special APIs
  const special = groups.get('Special APIs')
  if (special) {
    md += `## Special APIs\n\n`
    md += `| API | Mean |\n|-----|------|\n`
    for (const r of special) {
      md += `| ${r.name} | ${formatMs(r.mean)} |\n`
    }
    md += '\n'
  }

  // Batch Throughput
  const batch = groups.get('Batch Throughput')
  if (batch) {
    md += `## Batch Throughput\n\n`
    md += `| Scale | Mean | Items/sec |\n|-------|------|----------|\n`
    for (const r of batch) {
      const itemCount = parseInt(r.name.replace(/[^0-9]/g, ''))
      const itemsPerSec = Math.round(itemCount / (r.mean / 1000))
      md += `| ${r.name} | ${formatMs(r.mean)} | ${itemsPerSec.toLocaleString()} |\n`
    }
    md += '\n'
  }

  // Scaling
  const scalingLatin = groups.get('Scaling: text length → wrap time (maxWidth: 300px)')
  const scalingCJK = groups.get('Scaling: CJK text length → wrap time (maxWidth: 300px)')
  if (scalingLatin || scalingCJK) {
    md += `## Scaling: Text Length → Wrap Time\n\n`
    md += `| Chars | Latin | CJK |\n|-------|-------|-----|\n`
    const latinMap = new Map((scalingLatin ?? []).map(r => [r.name, r]))
    const cjkMap = new Map((scalingCJK ?? []).map(r => [r.name, r]))
    for (const name of ['10 chars', '100 chars', '1,000 chars', '10,000 chars']) {
      const l = latinMap.get(name)
      const c = cjkMap.get(name)
      md += `| ${name} | ${l ? formatMs(l.mean) : '—'} | ${c ? formatMs(c.mean) : '—'} |\n`
    }
    md += '\n'
  }

  // Overhead
  const overhead = groups.get('Overhead: Textric vs raw opentype.js')
  if (overhead) {
    md += `## Textric Overhead vs Raw opentype.js\n\n`
    md += `| Operation | Mean |\n|-----------|------|\n`
    for (const r of overhead) {
      md += `| ${r.name} | ${formatMs(r.mean)} |\n`
    }
    md += '\n'
  }

  md += `---\n\n*Generated with Textric benchmark suite. Run \`pnpm bench:report\` to regenerate on your machine.*\n`

  return md
}

// Main
console.log('Running benchmarks...')
const output = execSync('npx vitest bench --reporter=verbose 2>&1', {
  encoding: 'utf-8',
  maxBuffer: 10 * 1024 * 1024,
  timeout: 600_000,
  cwd: resolve(import.meta.dirname, '..'),
}).toString()

const groups = parseBenchOutput(output)
const report = generateReport(groups)

const outPath = resolve(import.meta.dirname, 'PERFORMANCE.md')
writeFileSync(outPath, report)
console.log(`\nReport written to ${outPath}`)
console.log(`${groups.size} benchmark groups captured.`)
